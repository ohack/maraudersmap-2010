	    <h1>The Marauder's Map @ Olin</h1>
	    <h3>76 Access Points + Software = Localization</h3>
	    <p>
	    	The Marauder's Map @ Olin is an opt-in program that utilizes Olin's wireless access points to generate a map of laptop locations on campus.  By training the system, we correlate access point signal strength to location.  Currently, this method is able to achieve approximately room-level accuracy.
	    </p>
